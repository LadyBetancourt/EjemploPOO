namespace EjemploPOO
{
    internal class OperacionesBasicas
    {
        public int Sumar(int primerNúmero, int segundoNúmero) 
        {
            return primerNúmero + segundoNúmero;
        }

        public int Restar(int primerNúmero, int segundoNúmero)
        {
            return primerNúmero - segundoNúmero;
        }

        public int Multiplicar(int primerNúmero, int segundoNúmero) 
        {
            return primerNúmero * segundoNúmero;
        }

        public decimal Division(int primerNúmero, int segundoNúmero) 
        {
            return primerNúmero / segundoNúmero;
        }
    }
}
